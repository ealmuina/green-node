void set_clock() {
  // Set time via NTP, as required for x.509 validation
  configTime(3 * 3600, 0, "pool.ntp.org", "time.nist.gov");
  time_t now = time(nullptr);
  while (now < 8 * 3600 * 2) {
    delay(500);
    now = time(nullptr);
  }
}

void update_firmware(String firmware_version) {
  set_clock();
  
  WiFiClientSecure wifi_client;
  wifi_client.setTrustAnchors(&cert);

  if (!wifi_client.connect(update_host, https_port)) {
    return;
  }

  ESPhttpUpdate.setAuthorization(updates_user, updates_password);
  ESPhttpUpdate.setLedPin(LED_BUILTIN, LOW);
  
  t_httpUpdate_return ret = ESPhttpUpdate.update(wifi_client, url_firmware_binary, firmware_version);

  switch (ret) {
    case HTTP_UPDATE_FAILED:
      break;

    case HTTP_UPDATE_NO_UPDATES:
      break;

    case HTTP_UPDATE_OK:
      break;
  }
}
